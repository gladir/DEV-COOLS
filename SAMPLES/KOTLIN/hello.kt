// hello.kt - Exemple Kotlin : println, val/var, interpolation de chaine

fun main() {
    val greeting: String = "Bonjour"
    var name: String = "Kotlin86"
    println("$greeting, $name!")

    val version: Int = 86
    println("Version : $version")

    var x: Int = 10
    x = x + 5
    println("x = $x")

    val flag: Boolean = true
    println("flag = $flag")
}
