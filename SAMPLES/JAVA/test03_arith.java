public class test03_arith {
    public static void main(String[] args) {
        int a;
        int b;
        int r;

        a = 20;
        b = 6;

        r = a + b;
        System.out.println(r);

        r = a - b;
        System.out.println(r);

        r = a * b;
        System.out.println(r);

        r = a / b;
        System.out.println(r);

        r = a % b;
        System.out.println(r);

        a += 5;
        System.out.println(a);

        a -= 3;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 4;
        System.out.println(a);

        a %= 3;
        System.out.println(a);

        a++;
        System.out.println(a);

        b--;
        System.out.println(b);
    }
}
