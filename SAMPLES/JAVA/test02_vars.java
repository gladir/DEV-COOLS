public class test02_vars {
    public static void main(String[] args) {
        int a;
        int b;
        boolean flag;
        char c;

        a = 42;
        b = -7;
        flag = true;
        c = 'X';

        System.out.println(a);
        System.out.println(b);
        System.out.println(flag);
        System.out.println(c);

        a = a + 1;
        System.out.println(a);

        flag = false;
        System.out.println(flag);
    }
}
